/**
 * 返回当前月份标签，格式 "YYYY年M月"（中文）或 "M/YYYY"（泰文）
 * 使用运行时 Date，避免硬编码月份字符串
 */
export function getCurrentMonthLabel(lang: "zh" | "th" = "zh"): string {
  const now = new Date();
  const year = now.getFullYear();
  const month = now.getMonth() + 1;
  if (lang === "th") {
    return `${month}/${year}`;
  }
  return `${year}年${month}月`;
}

